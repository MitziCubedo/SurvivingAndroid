package com.example.mitzicubedo.stores.models;

/**
 * Model that holds a store
 */
public class Store {

    private String mUrl;
    private String mName;

    public String getUrl() {
        return mUrl;
    }

    public void setUrl(String mUrl) {
        this.mUrl = mUrl;
    }

    public String getName() {
        return mName;
    }

    public void setName(String mName) {
        this.mName = mName;
    }
}